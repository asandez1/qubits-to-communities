def fig2_v3_circuit():
    fig, ax = plt.subplots(figsize=(14, 8))
    ax.set_xlim(0, 14)
    ax.set_ylim(-0.5, 20)
    ax.axis("off")

    N = 19
    y_positions = list(range(N))
    labels = [f"v1[{i}]" for i in range(9)] + [f"v2[{i-9}]" for i in range(9, 18)] + ["ancilla"]

    # Draw qubit lines
    for i, y in enumerate(y_positions):
        ax.plot([0.5, 13.5], [y, y], "k-", lw=0.8, alpha=0.4)
        ax.text(0.1, y, labels[i], ha="right", va="center", fontsize=8)

    # Layer 1: RY encoding (columns 1.0 - 1.8)
    for y in range(9):
        ax.add_patch(Rectangle((1.0, y - 0.25), 0.7, 0.5, facecolor="#D4E6F1", edgecolor="black"))
        ax.text(1.35, y, "RY", ha="center", va="center", fontsize=7)
    for y in range(9, 18):
        ax.add_patch(Rectangle((1.0, y - 0.25), 0.7, 0.5, facecolor="#FAD7A0", edgecolor="black"))
        ax.text(1.35, y, "RY", ha="center", va="center", fontsize=7)

    # Layer 1: trainable RY (2.5 - 3.2)
    for y in range(N):
        ax.add_patch(Rectangle((2.5, y - 0.25), 0.7, 0.5, facecolor="#ABEBC6", edgecolor="black"))
        ax.text(2.85, y, "θ₁", ha="center", va="center", fontsize=7)

    # CX to ancilla (Layer 1 entanglement, cols 4.0-4.5)
    for y in range(18):
        ax.plot([4.2, 4.2], [y, 18], "k-", lw=0.5)
        ax.plot(4.2, y, "ko", markersize=3)
    ax.plot(4.2, 18, "s", color="white", markeredgecolor="black", markersize=10)
    ax.text(4.2, 18.5, "⊕", ha="center", va="bottom", fontsize=10)

    # Layer 2: trainable RY (5.5-6.2)
    for y in range(N):
        ax.add_patch(Rectangle((5.5, y - 0.25), 0.7, 0.5, facecolor="#ABEBC6", edgecolor="black"))
        ax.text(5.85, y, "θ₂", ha="center", va="center", fontsize=7)

    # Cross-register CX (v1[i] ↔ v2[i])
    for i in range(9):
        ax.plot([7.2, 7.2], [i, i + 9], "r-", lw=0.8, alpha=0.7)
        ax.plot(7.2, i, "ro", markersize=3)
        ax.plot(7.2, i + 9, "s", color="white", markeredgecolor="red", markersize=6)

    # Layer 3: trainable RY (8.5-9.2)
    for y in range(N):
        ax.add_patch(Rectangle((8.5, y - 0.25), 0.7, 0.5, facecolor="#ABEBC6", edgecolor="black"))
        ax.text(8.85, y, "θ₃", ha="center", va="center", fontsize=7)

    # Final CX to ancilla from q0 and q9
    ax.plot([10.3, 10.3], [0, 18], "b-", lw=0.5)
    ax.plot(10.3, 0, "bo", markersize=4)
    ax.plot(10.3, 18, "s", color="white", markeredgecolor="blue", markersize=10)

    ax.plot([10.8, 10.8], [9, 18], "b-", lw=0.5)
    ax.plot(10.8, 9, "bo", markersize=4)
    ax.plot(10.8, 18, "s", color="white", markeredgecolor="blue", markersize=10)

    # Measurement
    ax.add_patch(Rectangle((12.5, 17.7), 0.7, 0.6, facecolor="#E5E8E8", edgecolor="black"))
    ax.text(12.85, 18, "M", ha="center", va="center", fontsize=9, fontweight="bold")

