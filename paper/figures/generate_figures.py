def _load_gini_series(path):
    """Extract per-architecture Gini series from exp9 JSON."""
    with open(path) as f:
        data = json.load(f)
    series = {}
    for arch in ["linear", "skill_only", "acceptance", "kc_9d", "kc_12d"]:
        if arch not in data:
            continue
        entries = data[arch].get("gini_series", [])
        cycles = [e[0] for e in entries]
        ginis = [e[1] for e in entries]
        series[arch] = (cycles, ginis)
    return series, data


