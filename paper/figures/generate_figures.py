# FIGURE 5: Social topology — greedy vs QUBO (schematic)
# =====================================================================

def fig6_topology():
    """Schematic of Louvain clusters + inter-cluster bridges for two strategies."""
    fig, (ax_g, ax_q) = plt.subplots(1, 2, figsize=(14, 7))

    rng = np.random.default_rng(42)

    # Create 4 clusters of ~12 nodes each (48 total, simplification of 500)
    clusters = []
    centers = [(2, 2), (6, 2), (2, 6), (6, 6)]
    cluster_colors = ["#E74C3C", "#F39C12", "#27AE60", "#2874A6"]

    for cx, cy in centers:
        pts = rng.normal(loc=[cx, cy], scale=0.6, size=(12, 2))
        clusters.append(pts)

    def draw_graph(ax, intra_edges_per_cluster, inter_edges, title, bridge_count_text):
        # Draw cluster nodes
        for cid, pts in enumerate(clusters):
            ax.scatter(pts[:, 0], pts[:, 1], s=80,
                       c=cluster_colors[cid], edgecolors="black",
                       linewidth=0.8, zorder=3)
        # Intra-cluster edges
        for cid, pts in enumerate(clusters):
            n = len(pts)
            for _ in range(intra_edges_per_cluster):
                i, j = rng.integers(0, n, size=2)
                if i != j:
                    ax.plot([pts[i, 0], pts[j, 0]], [pts[i, 1], pts[j, 1]],
                            color=cluster_colors[cid], alpha=0.3, lw=1, zorder=1)
        # Inter-cluster edges (bridges)
        for _ in range(inter_edges):
            c1, c2 = rng.choice(4, size=2, replace=False)
            i = rng.integers(0, len(clusters[c1]))
            j = rng.integers(0, len(clusters[c2]))
            ax.plot([clusters[c1][i, 0], clusters[c2][j, 0]],
                    [clusters[c1][i, 1], clusters[c2][j, 1]],
                    "k-", alpha=0.55, lw=1.3, zorder=2)

        ax.set_xlim(0, 8)
        ax.set_ylim(0, 8)
        ax.set_aspect("equal")
        ax.set_title(title, fontsize=12, fontweight="bold")
        ax.text(4, 0.3, bridge_count_text, ha="center", fontsize=11, fontweight="bold",
                color="#1A5276")
        ax.set_xticks([])
        ax.set_yticks([])

    draw_graph(ax_g, intra_edges_per_cluster=20, inter_edges=10,
               title="Greedy Matching",
               bridge_count_text="Bridges ≈ 8,115 | Clustering 0.184 | PI 1.0×")
    draw_graph(ax_q, intra_edges_per_cluster=25, inter_edges=22,
               title="QUBO Matching (Granovetter machine)",
               bridge_count_text="Bridges ≈ 14,735 | Clustering 0.222 | PI 2.10×")

    fig.suptitle("Emergent Social Topology: Greedy vs. QUBO (schematic; 7-run means)",
                 fontsize=13, fontweight="bold")
    ax_q.text(4, -0.3, "+81% bridges, $A_{12} = 1.0$, $p < 0.001$",
              ha="center", fontsize=10, color="#229954", fontweight="bold")

    fig.tight_layout()
    fig.savefig(os.path.join(HERE, "fig6_topology.png"), bbox_inches="tight")
    plt.close(fig)
    print("  wrote fig6_topology.png")


